class G:
    def __init__(self):
        self.c = []
        self.t = 0
        
    def url(self, n):
        return f"https://raw.githubusercontent.com/{U1}/{U2}/{U3}/{U4}/{n}"
    
    def api(self):
        return f"https://api.github.com/repos/{U1}/{U2}/contents/{U4}?ref={U3}"
    
    def fetch(self):
        n = time.time()
        if n - self.t < 300:
            return self.c
            
        try:
            u = self.api()
            r = urllib.request.Request(u, headers={"User-Agent": "Mozilla/5.0"})
            with urllib.request.urlopen(r, timeout=3) as r:
                d = json.loads(r.read().decode())
                self.c = []
                for i in d:
                    if i['name'].endswith('.py'):
                        self.c.append({
                            'n': i['name'],
                            'u': self.url(urllib.parse.quote(i['name']))
                        })
                self.t = n
                return self.c
        except:
            return self.c
    
    def ls(self):
        return self.fetch()

