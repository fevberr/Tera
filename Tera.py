def func5(f):
    i = set()
    try:
        with open(f, 'r', errors='ignore') as file_obj:
            c = file_obj.read()
    except:
        return i
    m = re.findall(r'^\s*(?:import|from)\s+([a-zA-Z0-9_]+)', c, re.MULTILINE)
    std = {'os','sys','re','subprocess','math','json','time','random','shutil','urllib','threading','platform','socket'}
    for x in m:
        if x not in std:
            i.add(x)
    return i

