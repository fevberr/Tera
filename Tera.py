    def mk(self):
        for d in [PROJ_DIR, DEFAULTS_DIR]:
            os.makedirs(d, exist_ok=True)
    
    def defs(self):
        a = os.path.join(DEFAULTS_DIR, "config.json")
        if not os.path.exists(a):
            with open(a, 'w') as f:
                json.dump(D, f, indent=2)
    
    def ls(self, d, e=None):
        if e:
            return [f for f in glob.glob(f"{d}/*.{e}") if os.path.isfile(f)]
        return [f for f in glob.glob(f"{d}/*") if os.path.isfile(f)]
    
    def rj(self, p):
        try:
            with open(p, 'r') as f:
                return json.load(f)
        except:
            return {}
    
    def wj(self, p, d):
        try:
            with open(p, 'w') as f:
                json.dump(d, f, indent=2)
            return True
        except:
            return False
    
    def rf(self, p):
        try:
            with open(p, 'r') as f:
                return f.read()
        except:
            return ""
    
    def wf(self, p, c):
        try:
            with open(p, 'w') as f:
                f.write(c)
            return True
        except:
            return False

