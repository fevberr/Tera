def func8(u):
    f = os.path.basename(urllib.parse.urlparse(u).path)
    if not f:
        f = "downloaded_file.py"
    
    if os.path.exists(f):
        try:
            os.remove(f)
        except:
            pass
    
    print(col("[*] Fetching...", "bright_cyan"))
    
    try:
        if shutil.which("wget"):
            subprocess.run(["wget", "-O", f, u], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL, check=False)
        elif shutil.which("curl"):
            subprocess.run(["curl", "-L", "-o", f, u], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL, check=False)
        else:
            urllib.request.urlretrieve(u, f)
        
        if os.path.exists(f) and os.path.getsize(f) > 0:
            return f
        else:
            return None
    except Exception as e:
        print(col(f"[-] Download failed: {str(e)}", "bright_red"))
        return None

